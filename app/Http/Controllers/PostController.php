    private function readBlogPostsFromFile($filePath)
{
    $posts = [];
    $lines = file($filePath, FILE_IGNORE_NEW_LINES);


    for ($i = 0; $i < count($lines); $i += 2) {
        $posts[] = [
            'title' => $lines[$i],
            'content' => $lines[$i + 1]
        ];
    }

    return $posts;
}

